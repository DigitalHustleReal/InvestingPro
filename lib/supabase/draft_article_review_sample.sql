- Fund manager track record
- Expense ratio
- Fund house reputation
- Investment style (large-cap, mid-cap, multi-cap)

### Step 3: Decide Investment Mode

**SIP (Systematic Investment Plan)**:
- Invest fixed amounts monthly
- Each SIP has separate 3-year lock-in
- Promotes disciplined investing
- Reduces timing risk

**Lump Sum**:
- One-time investment
- Single 3-year lock-in period
