- Online platforms (Zerodha, Groww, etc.)
- Distributors
- Banks

### Step 5: Claim Tax Deduction

While filing ITR:
- Declare ELSS investment under Section 80C
- Maximum deduction: ₹1.5 lakh (combined with other 80C investments)
- Keep investment receipts/statements

