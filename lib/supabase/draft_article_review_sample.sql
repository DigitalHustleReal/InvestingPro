## Frequently Asked Questions

### Q: What is the minimum investment in ELSS?

A: Most ELSS funds allow minimum investment of ₹500 through SIP or ₹5,000 as lump sum. Check specific fund requirements.

### Q: Can I invest more than ₹1.5 lakh in ELSS?

A: Yes, you can invest any amount, but tax deduction is limited to ₹1.5 lakh under Section 80C (combined with other 80C investments).

### Q: What happens if I need money before 3 years?

A: ELSS has mandatory 3-year lock-in. You cannot redeem before 3 years, even in emergencies. Plan accordingly.

### Q: Are ELSS returns guaranteed?

A: No, ELSS returns are not guaranteed. They depend on equity market performance. Past performance doesn''t guarantee future results.

### Q: Can I switch ELSS funds?

A: You can switch after lock-in period. Switching before lock-in is not allowed. Consider switching only if fund consistently underperforms.

### Q: How is ELSS taxed?

A: ELSS provides Section 80C deduction. Capital gains: 10% LTCG tax on gains above ₹1 lakh if held over 1 year (which lock-in ensures).

### Q: Should I invest in ELSS via SIP or lump sum?

A: SIP is better for most investors as it promotes discipline and reduces timing risk. Lump sum works if you have large funds and good market timing.

### Q: Can I invest in multiple ELSS funds?

A: Yes, you can invest in multiple ELSS funds. However, total Section 80C deduction remains ₹1.5 lakh across all investments.

