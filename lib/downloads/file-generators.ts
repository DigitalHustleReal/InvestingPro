/**
 * Generate CSV file
 */
export function generateCSVFromTemplate(template: DashboardTemplate): string {
    return generateExcelFromTemplate(template); // CSV and Excel (CSV format) are the same
}

/**
 * Generate Notion import instructions
 */
export function generateNotionInstructions(template: DashboardTemplate): string {
    return `
# ${template.name} - Notion Setup Guide

## Step 1: Create a New Database
1. In Notion, create a new database (Table view)
2. Name it: "${template.name}"

## Step 2: Add Columns
Add these columns to your Notion database:

${template.columns.map((col, index) => `
${index + 1}. **${col.name}**
   - Type: ${mapColumnTypeToNotion(col.type)}
   - ${col.description || 'No description'}
`).join('')}

## Step 3: Import Sample Data
1. Copy the sample data below
2. Paste into your Notion database
3. Adjust formulas as needed (Notion uses different formula syntax)

## Sample Data Structure
${template.columns.map(col => col.name).join(' | ')}
${template.columns.map(() => '---').join(' | ')}
${template.sampleData && template.sampleData.length > 0 ? template.sampleData.map(row => row.join(' | ')).join('\n') : 'No sample data'}

## Formulas (Convert to Notion Syntax)
${template.formulas ? template.formulas.map(f => `- ${f.cell}: ${f.formula} - ${f.description}`).join('\n') : 'No formulas'}

## Tips
- Notion formulas use different syntax than Excel
- Use Notion's formula editor to convert Excel formulas
- You can import CSV data directly into Notion
- Use Notion's templates feature to save this as a template

---
Generated by InvestingPro.in
    `.trim();
}

function mapColumnTypeToNotion(excelType: string): string {
    const mapping: Record<string, string> = {
        'text': 'Text',
        'number': 'Number',
        'date': 'Date',
        'currency': 'Number (with currency format)',
        'percentage': 'Number (with percentage format)',
        'formula': 'Formula'
    };
    return mapping[excelType] || 'Text';
}
