/**
 * File Generators
 * Generates Excel, PDF, CSV files from templates
 */

import { DashboardTemplate } from './dashboard-templates';

/**
 * Generate Excel file from template (CSV format for now)
 * TODO: Install ExcelJS for real .xlsx files
 */
export function generateExcelFromTemplate(template: DashboardTemplate): string {
    const lines: string[] = [];
    
    // Add headers
    const headers = template.columns.map(col => col.name);
    lines.push(headers.join(','));
    
    // Add sample data if available
    if (template.sampleData && template.sampleData.length > 0) {
        template.sampleData.forEach(row => {
            const formattedRow = row.map((cell: any) => {
                // Escape commas and quotes in CSV
                const cellStr = String(cell);
                if (cellStr.includes(',') || cellStr.includes('"') || cellStr.includes('\n')) {
                    return `"${cellStr.replace(/"/g, '""')}"`;
                }
                return cellStr;
            });
            lines.push(formattedRow.join(','));
        });
    }
    
    // Add formulas as comments (Excel will need manual setup)
    if (template.formulas && template.formulas.length > 0) {
        lines.push('');
        lines.push('Formulas:');
        template.formulas.forEach(formula => {
            lines.push(`Cell ${formula.cell}: ${formula.formula} - ${formula.description}`);
        });
    }
    
    return lines.join('\n');
}

